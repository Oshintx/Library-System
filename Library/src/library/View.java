    public void memberSerialize(SetOfMembers SOM) {
        try {
            FileOutputStream mfos = new FileOutputStream(new File("memberDB.txt"));
            ObjectOutputStream moos = new ObjectOutputStream(mfos);
            moos.writeObject(SOM);
            moos.flush();
            moos.close();
        } catch (Exception e) {
            System.out.println(e);
        }
    }

